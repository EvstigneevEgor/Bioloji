package com.company.domain

/**
 * Направления 1..8 как чистые данные домена: только геометрия (смещения по
 * полю) и валидность символа-аргумента. Презентация направления (стрелки,
 * русские названия, компас) живёт в UI ([[com.company.ui.DirectionView]]).
 *
 * Нумерация (y растёт вниз): 1=вверх, 2=вверх-вправо, 3=вправо, 4=вниз-вправо,
 * 5=вниз, 6=вниз-влево, 7=влево, 8=вверх-влево.
 */
object Direction:
  /** Смещение (dx, dy) для направления 1..8; для прочего — (0, 0). */
  def offset(dir: Int): (Int, Int) = dir match
    case 1 => (0, -1)
    case 2 => (1, -1)
    case 3 => (1, 0)
    case 4 => (1, 1)
    case 5 => (0, 1)
    case 6 => (-1, 1)
    case 7 => (-1, 0)
    case 8 => (-1, -1)
    case _ => (0, 0)

  /** Является ли символ цифрой-направлением 1..8. */
  def isArg(c: Char): Boolean = c >= '1' && c <= '8'

  /** Номер направления из символа-цифры (без проверки — см. [[isArg]]). */
  def of(c: Char): Int = c - '0'
